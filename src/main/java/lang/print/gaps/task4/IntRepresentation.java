package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char ch = 'c';
        char ch2 = 'h';
        char ch3 = 'a';
        char ch4 = 'r';
        int c = ch;
        int h = ch2;
        int a = ch3;
        int r = ch4;

        System.out.println(c);
        System.out.println(h);
        System.out.println(a);
        System.out.println(r);

    }
}
